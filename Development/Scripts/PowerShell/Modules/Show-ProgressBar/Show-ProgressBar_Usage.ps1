Import-Module "$PSScriptRoot\Show-ProgressBar.psm1" -Force

# --- Demonstration ---
Write-Host "Replicating Docker-style progress bar (PowerShell with Braille):"
$totalSize = 10.02
foreach ($i in 0..100) {
    $progressPercentage = $i / 100.0
    $barString = Show-ProgressBar -Progress $progressPercentage -Width 30

    # Simulate download progress text
    $downloaded = $totalSize * $progressPercentage
    $status = "{0} {1:N3}MB / {2:N2}MB" -f $barString, $downloaded, $totalSize
    
    # Use Write-Host with -NoNewline and carriage return to overwrite the line
    Write-Host "`r$status" -NoNewline
    Start-Sleep -Milliseconds 50
}

Write-Host "" # Move to the next line after completion
Write-Host "Download complete."

